package com.udacity.study.jam.radiotastic.sync;

import android.app.Service;
import android.content.Intent;
import android.os.IBinder;

public class RadiotasticSyncService extends Service {
    private static final Object sSyncAdapterLock = new Object();
    private static RadiotasticSyncAdapter sSunshineSyncAdapter = null;

    @Override
    public void onCreate() {
        synchronized (sSyncAdapterLock) {
            if (sSunshineSyncAdapter == null) {
                sSunshineSyncAdapter = new RadiotasticSyncAdapter(getApplicationContext(), true);
            }
        }
    }

    @Override
    public IBinder onBind(Intent intent) {
        return sSunshineSyncAdapter.getSyncAdapterBinder();
    }
}